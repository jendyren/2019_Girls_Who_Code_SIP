#Len is the keyword that lets programmers get the size of a list
aList = [0, 1, 2, 3, 4]
print(len(aList))
# Output: 5

aList = [7, 6, 5, 4, 3]
print(len(aList))
#Output: 5

#in is the keyword that lets programmrs easily check if something is in a list. (Boolean = Only two outcomes: True or False)
aList = [0, 1, 2, 3, 4]
print(5 in aList)
#Output: False
print(4 in aList)
#Output: True

numbers = [1, 2, 3, 2, 1]

